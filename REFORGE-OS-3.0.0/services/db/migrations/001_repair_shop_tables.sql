-- Migration: Repair Shop Management Tables
-- Adds case management, device inventory, jobs, and artifacts to existing ForgeWorks schema
-- This extends the analysis-focused schema with repair shop workflow tables

CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- Cases: Repair jobs/tickets
CREATE TABLE IF NOT EXISTS cases (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  created_by TEXT NOT NULL,
  customer_name TEXT,
  customer_email TEXT,
  customer_phone TEXT,
  status TEXT NOT NULL DEFAULT 'intake' CHECK (status IN ('intake','in-progress','waiting-parts','completed','closed')),
  notes TEXT,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Repair Devices: Device inventory linked to cases
-- Note: This is separate from the analysis 'devices' table to support repair workflows
CREATE TABLE IF NOT EXISTS repair_devices (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  case_id UUID REFERENCES cases(id) ON DELETE CASCADE,
  platform TEXT NOT NULL CHECK (platform IN ('android','ios','unknown')),
  model TEXT,
  serial TEXT,
  imei TEXT,
  os_version TEXT,
  connection_state TEXT,  -- 'usb', 'none', 'recovery', 'dfu', 'fastboot'
  trust_state JSONB DEFAULT '{}',  -- {adb_authorized: bool, fastboot_unlocked: bool, paired: bool}
  passport JSONB DEFAULT '{}',     -- Full device metadata snapshot
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Jobs: Workflow executions
CREATE TABLE IF NOT EXISTS jobs (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  case_id UUID REFERENCES cases(id) ON DELETE CASCADE,
  workflow_id TEXT NOT NULL,
  status TEXT NOT NULL DEFAULT 'queued' CHECK (status IN ('queued','running','failed','succeeded')),
  actor TEXT NOT NULL,
  started_at TIMESTAMPTZ,
  finished_at TIMESTAMPTZ,
  error TEXT,
  metadata JSONB DEFAULT '{}',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Audit Events: Detailed activity log for jobs
-- Separate from audit_logs table which is for analysis platform
CREATE TABLE IF NOT EXISTS audit_events (
  id BIGSERIAL PRIMARY KEY,
  job_id UUID REFERENCES jobs(id) ON DELETE CASCADE,
  timestamp TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  level TEXT NOT NULL CHECK (level IN ('info','warn','error')),
  action TEXT,
  message TEXT,
  stdout TEXT,
  stderr TEXT,
  exit_code INTEGER,
  metadata JSONB DEFAULT '{}'
);

-- Artifacts: Reports, bundles, logs generated by jobs
CREATE TABLE IF NOT EXISTS artifacts (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  job_id UUID REFERENCES jobs(id) ON DELETE CASCADE,
  case_id UUID REFERENCES cases(id) ON DELETE SET NULL,
  kind TEXT NOT NULL,  -- 'report', 'bundle', 'log', 'passport', 'diagnostics'
  path TEXT NOT NULL,
  sha256 TEXT,
  size_bytes BIGINT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Indexes for performance
CREATE INDEX IF NOT EXISTS idx_cases_status ON cases(status);
CREATE INDEX IF NOT EXISTS idx_cases_created_at ON cases(created_at DESC);
CREATE INDEX IF NOT EXISTS idx_repair_devices_case_id ON repair_devices(case_id);
CREATE INDEX IF NOT EXISTS idx_repair_devices_platform ON repair_devices(platform);
CREATE INDEX IF NOT EXISTS idx_repair_devices_serial ON repair_devices(serial) WHERE serial IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_jobs_case_id ON jobs(case_id);
CREATE INDEX IF NOT EXISTS idx_jobs_status ON jobs(status);
CREATE INDEX IF NOT EXISTS idx_jobs_workflow_id ON jobs(workflow_id);
CREATE INDEX IF NOT EXISTS idx_audit_events_job_id ON audit_events(job_id);
CREATE INDEX IF NOT EXISTS idx_audit_events_timestamp ON audit_events(timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_artifacts_job_id ON artifacts(job_id);
CREATE INDEX IF NOT EXISTS idx_artifacts_case_id ON artifacts(case_id);
CREATE INDEX IF NOT EXISTS idx_artifacts_kind ON artifacts(kind);

-- Function to update updated_at timestamp
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Triggers for updated_at
CREATE TRIGGER update_cases_updated_at BEFORE UPDATE ON cases
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_repair_devices_updated_at BEFORE UPDATE ON repair_devices
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

-- Comments for documentation
COMMENT ON TABLE cases IS 'Repair shop cases/tickets tracking customer jobs';
COMMENT ON TABLE repair_devices IS 'Device inventory for repair cases (separate from analysis devices table)';
COMMENT ON TABLE jobs IS 'Workflow execution jobs linked to cases';
COMMENT ON TABLE audit_events IS 'Detailed audit log for job executions (append-only)';
COMMENT ON TABLE artifacts IS 'Generated reports, bundles, and logs from jobs';
